const User = require('./User')

class PrivilegedUser extends User {
  constructor(arg = {}) {
    // pass along all properties, overriding id
    super(Object.assign(arg, { _is_privileged: true, id: 'privileged' }))
    Object.defineProperty(this, 'is', {
      get () {
        return () => {
          return true
        }
      }
    })
  }
}

module.exports = PrivilegedUser
